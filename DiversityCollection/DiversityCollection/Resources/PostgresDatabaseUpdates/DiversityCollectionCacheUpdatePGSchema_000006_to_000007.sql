ALTER TABLE "#project#"."CacheIdentification" ADD "BaseURL" character varying(255) NULL;

--#####################################################################################################################
--######   version   ##################################################################################################
--#####################################################################################################################

CREATE OR REPLACE FUNCTION "#project#".version()
  RETURNS integer AS
$BODY$
declare
	v integer;
BEGIN
   SELECT 7 into v;
   RETURN v;
END;
$BODY$
  LANGUAGE plpgsql STABLE
  COST 100;
